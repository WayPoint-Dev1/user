CREATE TABLE USER_MANAGEMENT.USER_TRIP_MAP (
    ID UUID DEFAULT "user_management".uuid_generate_v4(),
    USER_TRIP_MAP_ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    USER_ID UUID NOT NULL,
    TRIP_ID UUID NOT NULL,
    IS_ACTIVE BOOLEAN NOT NULL,
    CREATED_ON TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CREATED_BY VARCHAR(100) NOT NULL,
    UPDATED_ON TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UPDATED_BY VARCHAR(100) NOT NULL,
    CONSTRAINT USER_TRIP_MAP_PKEY PRIMARY KEY (ID),
    CONSTRAINT USER_ID_FKEY FOREIGN KEY (USER_ID) REFERENCES USER_MANAGEMENT.USER_INFO(ID)
);